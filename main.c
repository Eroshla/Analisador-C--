#include <stdio.h>

int main() {
    // Print "Hello, World!" to test the program
    printf("Hello, World!\n");

    // Example of an if statement
    if (1) {
        printf("This is inside an if statement.\n");
    }

    // Example of a for loop
    for (int i = 0; i < 5; i++) {
        printf("Loop iteration: %d\n", i);
    }

    return 0;
}